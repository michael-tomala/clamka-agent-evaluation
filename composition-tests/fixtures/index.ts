/**
 * Eksport zbiorczy wszystkich fixtures kompozycji
 */

import type { CompositionTestFixture } from '../types';
import { generateFixturesFromDefinitions } from './fromDefinitions';
import { subscribeCtaFixtures } from './subscribeCta';
import { youtubeSubscribeCardFixtures } from './youtubeSubscribeCard';
import { xPostFixtures } from './xPost';
import { blurBackgroundImageFixtures } from './blurBackgroundImage';
import { facebookPostFixtures } from './facebookPost';

/**
 * Zwraca wszystkie fixtures - auto-generowane z definicji + manualne warianty
 */
export function getAllFixtures(): CompositionTestFixture[] {
  const autoGenerated = generateFixturesFromDefinitions();

  const manual: CompositionTestFixture[] = [
    ...subscribeCtaFixtures,
    ...youtubeSubscribeCardFixtures,
    ...xPostFixtures,
    ...blurBackgroundImageFixtures,
    ...facebookPostFixtures,
  ];

  return [...autoGenerated, ...manual];
}

/**
 * Zwraca fixtures dla konkretnej kompozycji
 */
export function getFixturesByDefinitionId(definitionId: string): CompositionTestFixture[] {
  return getAllFixtures().filter(f => f.compositionDefinitionId === definitionId);
}

/**
 * Zwraca fixture po ID
 */
export function getFixtureById(fixtureId: string): CompositionTestFixture | undefined {
  return getAllFixtures().find(f => f.id === fixtureId);
}
